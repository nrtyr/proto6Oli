			<p>
				El H. Ayuntamiento Nicolás Romero, con domicilio para efectos del presente aviso en Av.
Juárez, s/n, colonia Centro, C.P. 54400, Nicolás Romero, Estado de México; a través de las
dependencias administrativas que se encuentran adscritas a este Sujeto Obligado, es el responsable del
uso, protección y tratamiento de sus Datos Personales, observando íntegramente para ello lo previsto
en la Ley de Protección de Datos Personales del Estado de México y los Lineamientos por los que se
establecen las políticas, criterios y procedimientos que deberán de observar los Sujetos Obligados, para
proveer la aplicación e implementación de la Ley de Protección de Datos Personales del Estado de
México.
			</p>
			<p>
				Se entiende que existe un consentimiento para su protección y tratamiento según corresponda en los
sistemas de Datos Personales que administran cada una de las dependencias administrativas, en los
términos citados en este aviso.
			</p>
			<p>
				Este Ayuntamiento manifiesta no transmitir sus datos personales a persona física o jurídico colectiva
alguna que sea ajena, sin su consentimiento expreso; notificándole en su caso qué datos serán
transmitidos, cual es la finalidad de dicho trámite y quién es el destinatario.
			</p>
			<p>
				La entrega de los datos personales es facultativa, en caso de que el titular se negara a otorgarlos, se
generará como consecuencia el no estar en posibilidades de realizar el trámite que pretende llevar a
cabo.
			</p>
			<p>
				Para la mejor comprensión del presente aviso de privacidad, se realizan las siguientes preguntas:
			</p>
			<p>
				<b>¿Qué son los datos personales?</b>
			</p>
			<p>
				Cualquier información concerniente a una persona física identificada o identificable.
			</p>
			<p>
				<b>¿Para qué fines se recabarán sus datos personales?</b>
			</p>
			<p>
				Para trámites administrativos, prestación de servicios, altas de servidores públicos y actualización de la situación laboral de cada uno, contrataciones y adquisición de bienes y servicios.
			</p>
			<p>
				<b>¿Qué datos personales se recabarán?</b>
			</p>
			<p>
				Para llevar a cabo las finalidades descritas en el presente aviso de privacidad y dependiendo específicamente del trámite a realizar, se utilizarán, de manera enunciativa más no limitativa, los siguientes datos personales.
			</p>
			<p>
				- Datos de identificación como: nombre, número de cuenta, estado civil, firma autógrafa y electrónica, registro federal de contribuyentes (RFC), clave única de registro de población (CURP), Clave de ISSEMYM, nacionalidad, fecha de nacimiento, datos contenidos en acta de nacimiento, datos familiares, fotografía, imagen, voz entre otros.
			</p>
			<p>
				- Datos de contacto como: domicilio, números telefónicos fijos o celulares o correos electrónicos de índole particular, entre otros.
			</p>
			<p>
				- Datos académicos como: Grado máximo de estudios.
			</p>
			<p>
				- Datos patrimoniales o financieros.
			</p>
			<p>
				<b>¿Cómo puede acceder, rectificar o cancelar sus datos personales, u oponerse a su uso?</b>
			</p>
			<p>
				Usted tiene derecho a conocer qué datos personales tenemos, para qué los utilizamos y las condiciones del uso que les damos (Acceso). Asimismo, es su derecho solicitar la corrección de su información personal cuando esté incompleta, sea inexacta, inadecuada o excesiva (Rectificación); que se elimine de nuestros registros o bases de datos cuando considere que su tratamiento contraviene lo dispuesto por la Ley señalada o porque dejó de ser necesaria para el cumplimiento de la finalidad o finalidades de dicha base (Cancelación); así como oponerse al uso de sus datos personales para fines específicos (Oposición). Estos derechos se conocen como derechos ARCO.
			</p>
			<p>	
				Por lo que Usted podrá ejercer dichos derechos en los términos previstos por la Ley de Protección de Datos Personales del Estado de México.
			</p>
			<p>
				<b>¿Cómo puede revocar su consentimiento para el uso de sus datos personales?</b>
			</p>
			<p>
				Usted puede revocar el consentimiento que, en su caso, haya otorgado para el tratamiento de sus datos personales. Sin embargo, es importante considerar que no en todos los casos se podrá atender y/o concluir dicha solicitud de forma inmediata, ya que es posible que por alguna obligación legal o administrativa sea necesario continuar tratando sus datos personales. Aunado a esto, debe considerar que esta acción puede ocasionar que no se le siga prestando el servicio solicitado o concluya el trámite que haya iniciado con este Ayuntamiento.
			</p>
			<p>
				Para tal efecto puede ejercer este derecho por escrito o de manera verbal, o bien para realizar cualquiera de estos procedimientos, así como recibir asesoría por parte de alguna dependencia de este Ayuntamiento, el titular podrá acudir a las oficinas de la Unidad de Información, Planeación, Programación y Evaluación o a la Unidad de Transparencia, localizadas en Av. Juárez, s/n, colonia Centro, C.P. 54400, Nicolás Romero, Estado de México, tel. 58237125.
			</p>
			<p>
				El interesado podrá dirigirse al Instituto de Transparencia, Acceso a la información Pública y Protección de Datos Personales del Estado de México y Municipios, donde recibirá asesoría sobre los derechos que tutela la Ley de Protección de Datos Personales del Estado de México, el cual se encuentra ubicado en calle de Pino Suárez s/n, actualmente Carretera Toluca-Ixtapan # 111, Colonia La Michoacana; Metepec Estado de México, C.P. 52166, tel. 01 (722) 226 1980.
